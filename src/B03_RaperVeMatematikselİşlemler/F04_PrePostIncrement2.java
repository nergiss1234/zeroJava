package B03_RaperVeMatematikselİşlemler;

public class F04_PrePostIncrement2 {
    public static void main(String[] args) {

        int a=10;
        System.out.println("a 'nın değeri : "+ ++a); //11

        int b=a++;
        System.out.println("b'nin değeri : "+b);//b=11 a=12

        int c=b++ +a;
        System.out.println("c'nin değeri : "+c);//23 b=12

        System.out.println("son toplam "+(a+b+c));//23+12+12=47
















    }














}
