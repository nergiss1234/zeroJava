package B03_RaperVeMatematikselİşlemler;

public class F01_WraperClass {
    public static void main(String[] args) {
        String nutellaFiyat1="23";
        String nutellaFiyat2="45";

        //String olarak verilen iki ürünün fiyatını toplayın....

        System.out.println(nutellaFiyat1+nutellaFiyat2);


        //İçinde SADECE sayılar olduğundan emin olduğunuz Stringleri
        //Wrapper class'ları kullanarak sayıya çevirebiliriz....

        int fiy1=Integer.parseInt(nutellaFiyat1);
        int fiy2=Integer.parseInt(nutellaFiyat2);
        System.out.println(fiy1+fiy2);







    }
























}
