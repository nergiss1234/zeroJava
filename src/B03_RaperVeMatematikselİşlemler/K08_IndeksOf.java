package B03_RaperVeMatematikselİşlemler;

public class K08_IndeksOf {
    public static void main(String[] args) {
        String str="java zamanla güzelleşir";
        //indeksOf metodu bize aradığımızın nerde olduğunu gösterir
        //str de ilk a harfinin nerde olduğunu bulunuz...
        System.out.println(str.indexOf("a"));//1
        System.out.println(str.indexOf('z'));//5
        System.out.println(str.indexOf("z", 5));





    }
























}
